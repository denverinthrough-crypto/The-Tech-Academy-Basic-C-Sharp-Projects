using System;
using System.Collections.Generic;
using System.Text;

namespace MathConsoleApp
{
    public class MathOperations
    {
        // Multiplies the number by 2 and returns the result
        public int Double(int number)
        {
            return number * 2;
        }

        // Multiplies the number by itself and returns the result
        public int Square(int number)
        {
            return number * number;
        }

        // Adds 100 to the number and returns the result
        public int AddOneHundred(int number)
        {
            return number + 100;
        }


    }
}
